# กำหนดเสียงผู้หญิงจีน (สำเนียงธรรมชาติ)
VOICE = "zh-CN-XiaoxiaoNeural"
OUTPUT_DIR = "audio"

async def generate_audio(text, filename, semaphore):
    """สร้างไฟล์เสียงทีละไฟล์ พร้อมตัวหน่วง (Semaphore) ป้องกันการเชื่อมต่อโดนแบน"""
    async with semaphore:
        try:
            # เพิ่ม rate="-10%" เพื่อลดความเร็วลง 10% (ปรับตัวเลขได้ตามต้องการ)
            communicate = edge_tts.Communicate(text, VOICE, rate="-10%")
            await communicate.save(filename)
            print(f"✅ สร้างสำเร็จ: {filename}")
        except Exception as e:
            print(f"❌ ล้มเหลว {filename} ({text}): {e}")

async def main():
    # สร้างโฟลเดอร์ audio หากยังไม่มี
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    
    # โหลดพร้อมกันสูงสุด 10 ไฟล์ 
    semaphore = asyncio.Semaphore(10)
    tasks = []
    
    # คำนวณจำนวนประโยคทั้งหมด
    total_sentences = sum(len(sentences) for sentences in sentence_data.values())
    
    print(f"🚀 กำลังเริ่มสร้างไฟล์เสียงทั้งหมด {total_sentences} ไฟล์...")
    
    # วนลูปตามหมวดหมู่และประโยค
    for category, sentences in sentence_data.items():
        for index, text in enumerate(sentences):
            # ตั้งชื่อไฟล์ เช่น basic_0.mp3, shopping_1.mp3
            filename = os.path.join(OUTPUT_DIR, f"{category}_{index}.mp3")
            
            # ถ้ามีไฟล์นี้อยู่แล้วให้ข้ามไป
            if os.path.exists(filename):
                print(f"⏭️ ข้าม (มีไฟล์แล้ว): {filename}")
                continue
                
            tasks.append(generate_audio(text, filename, semaphore))
    
    # รันการสร้างไฟล์เสียงทั้งหมด
    if tasks:
        await asyncio.gather(*tasks)
        print(f"\n🎉 ดาวน์โหลดไฟล์เสียงทั้งหมด {total_sentences} ประโยค เสร็จเรียบร้อย!")
    else:
        print("\n✨ ไฟล์เสียงทั้งหมดมีครบอยู่แล้ว!")

if __name__ == "__main__":
    asyncio.run(main())